// Given array
const array = [1, 2, 3, 4];

// Initialize a variable to hold the sum
let sum = 0;

// Iterate through each element of the array and add it to the sum
for (let i = 0; i < array.length; i++) {
    sum += array[i];
}

// Output the sum
console.log("Sum of all numbers in the array:", sum);
